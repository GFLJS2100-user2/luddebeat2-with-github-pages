function _(...i) {
	throw i.join('\n--------------------\n');
};

function g(s, v, t, n = 0) {
	return t < s ? n ? '\0 \0 \0 ' : 0 : v;
};

function $(q) {
	return round(log(q)/log(2)*12);
};

